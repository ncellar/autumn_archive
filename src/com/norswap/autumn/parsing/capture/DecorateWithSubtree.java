package com.norswap.autumn.parsing.capture;

public final class DecorateWithSubtree implements Decorate
{
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public final ParseTree subtree;

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public DecorateWithSubtree(ParseTree subtree)
    {
        this.subtree = subtree;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    @Override
    public void decorate(ParseTree tree)
    {
        tree.accessor = subtree.accessor;
        tree.value = subtree.value;
        tree.kind = subtree.kind;
        tree.kinds = subtree.kinds;
        tree.children = subtree.children;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////

    @Override
    public String toString()
    {
        return "subtree(" + subtree.nodeToString() + ")";
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
}
