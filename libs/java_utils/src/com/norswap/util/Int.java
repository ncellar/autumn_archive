package com.norswap.util;

/**
 * A simple modifiable boxed integer, for use inside lambdas and such.
 * <p>
 * e.g.:
 * <pre>
 *      Int counter = new Int();
 *      Arrays.stream(array).forEach(x -> counter.i++);
 * </pre>
 */
public class Int
{
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public int i;

    ////////////////////////////////////////////////////////////////////////////////////////////////

    public Int() {}

    // ---------------------------------------------------------------------------------------------

    public Int(int i)
    {
        this.i = i;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
}
